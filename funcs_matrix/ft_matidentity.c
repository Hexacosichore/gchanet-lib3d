#include "../lib3d.h"

void	ft_matidentity(t_mat *mat)
{
	ft_matbzero(mat);
	mat->x[0] = 1;
	mat->y[1] = 1;
	mat->z[2] = 1;
	mat->w[3] = 1;
}
